package org.lqscr88.utils.distance;

public class Point {
    // 成员变量
    private double x;
    private double y;

    // 构造方法
    Point(){

    }

    public Point(double x,double y) {
        this.x = x;
        this.y = y;

    }

    // 成员方法
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }



}